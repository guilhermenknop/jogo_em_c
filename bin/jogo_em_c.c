int xc=0;
int yc=0;

int coins=0;
int vida=9;

void vercenario(){
    xc=0;
    yc=0;
    while(xc<60){
        while(yc<20){
            mvaddch(yc, xc, cenario[yc][xc]);
            yc++;
        }
        xc++;
        yc=0;
    }
    
}

int xp=2;
int yp=2;

void verp(){
    mvaddch(yp, xp, '@');
}



int t;

void moverp() {

    t = getch();

    switch(t) {

        case KEY_UP:

            if(cenario[yp - 1][xp] != '#') {

                yp--;

                if(cenario[yp][xp] == '$') {

                    coins++;

                    cenario[yp][xp] = ' ';
                }

                if(cenario[yp][xp] == '*') {

                    vida--;

                    cenario[yp][xp] = ' ';
                }
            }

            break;

        case KEY_DOWN:

            if(cenario[yp + 1][xp] != '#') {

                yp++;

                if(cenario[yp][xp] == '$') {

                    coins++;

                    cenario[yp][xp] = ' ';
                }

                if(cenario[yp][xp] == '*') {

                    vida--;

                    cenario[yp][xp] = ' ';
                }
            }

            break;

        case KEY_LEFT:

            if(cenario[yp][xp - 1] != '#') {

                xp--;

                if(cenario[yp][xp] == '$') {

                    coins++;

                    cenario[yp][xp] = ' ';
                }

                if(cenario[yp][xp] == '*') {

                    vida--;

                    cenario[yp][xp] = ' ';
                }
            }

            break;

        case KEY_RIGHT:

            if(cenario[yp][xp + 1] != '#') {

                xp++;

                if(cenario[yp][xp] == '$') {

                    coins++;

                    cenario[yp][xp] = ' ';
                }

                if(cenario[yp][xp] == '*') {

                    vida--;

                    cenario[yp][xp] = ' ';
                }
            }

            break;
    }
}

